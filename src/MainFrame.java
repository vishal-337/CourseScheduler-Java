    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String result = "Scheduled students dropped from the course: "+"\n";
        String courseid=jComboBox6.getSelectedItem().toString();
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseid);
        for (ScheduleEntry i:schedules)
        {
            StudentEntry x =(StudentQueries.getStudent(i.getStudentID()));
            result=result+x.getLastName()+","+x.getFirstName()+" "+x.getStudentID()+"has been dropped from "+courseid+"\n";
        }
        ArrayList<ScheduleEntry> schedulew = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseid);
        result = result+"\n"+ "Waitlisted students dropped from the course: "+ "\n";
        for (ScheduleEntry i:schedulew)
        {
            StudentEntry x =(StudentQueries.getStudent(i.getStudentID()));
            result=result+x.getLastName()+","+x.getFirstName()+" "+x.getStudentID()+"has been dropped from "+courseid+"\n";
        }
        CourseQueries.dropCourse(currentSemester, courseid);
        ScheduleQueries.dropScheduleByCourse(currentSemester, courseid);
        jTextArea3.setText(result);
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_jButton8ActionPerformed

