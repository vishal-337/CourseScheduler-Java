    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String semester = currentSemester;
        String CourseCode = jComboBox2.getSelectedItem().toString();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String StudentID = (students.get(jComboBox1.getSelectedIndex())).getStudentID();
        String Status;
        int x = ScheduleQueries.getScheduledStudentCount(semester, CourseCode);
        int y = CourseQueries.getCourseSeats(semester, CourseCode);
        if (x>=y){
            Status="W";
            jLabel16.setText(jComboBox1.getSelectedItem().toString()+" has been waitlisted for the class");
        }
        else
        {
            Status="S";
            jLabel16.setText(jComboBox1.getSelectedItem().toString()+" has been scheduled for the class");
        }
        ScheduleQueries.addScheduleEntry(new ScheduleEntry(semester,CourseCode,StudentID,Status));
        rebuildCourseComboBoxes1();
    }//GEN-LAST:event_jButton3ActionPerformed

